def normalize_language(language):
    return language if language in LANGUAGES else DEFAULT_LANGUAGE


def translate(key, language):
    language = normalize_language(language)
    value = UI.get(key)
    if not value:
        return key
    return value.get(language) or value.get(DEFAULT_LANGUAGE) or key


def label(mapping, key, language):
    language = normalize_language(language)
    value = mapping.get(str(key), {})
    return value.get(language) or value.get(DEFAULT_LANGUAGE) or str(key)


def profession_title(profession, language):
    language = normalize_language(language)
    if language == "en":
        return profession.title_en
    if language == "ky":
        return PROFESSIONS_KY.get(profession.slug, {}).get("title") or profession.title_ru
    return profession.title_ru


def profession_description(profession, language):
    language = normalize_language(language)
    if language == "en":
        return profession.description_en
    if language == "ky":
        return PROFESSIONS_KY.get(profession.slug, {}).get("description") or profession.description_ru
    return profession.description_ru
